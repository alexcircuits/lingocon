import type { Metadata } from "next";
import "./globals.css";
import { getSiteUrl } from "@/lib/seo";
import { Toaster } from "@/components/ui/sonner";
import { AchievementListener } from "@/components/achievement-listener";
import { SessionProvider } from "next-auth/react";
import { ThemeProvider } from "@/components/theme-provider";
import { Instrument_Serif, JetBrains_Mono, Noto_Sans, Plus_Jakarta_Sans } from "next/font/google";
import localFont from "next/font/local";
import { NextIntlClientProvider } from "next-intl";
import { getLocale, getMessages, getTranslations } from "next-intl/server";

const instrumentSerif = Instrument_Serif({
  weight: ["400"],
  style: ["normal", "italic"],
  subsets: ["latin"],
  variable: "--font-instrument-serif",
  display: "swap",
});

const jetBrainsMono = JetBrains_Mono({
  weight: ["400", "500"],
  subsets: ["latin"],
  variable: "--font-jetbrains-mono",
  display: "swap",
});

// Geometric sans used by the redesigned (Aurora) landing page. Scoped via CSS var;
// existing pages keep their system-ui body font.
const plusJakarta = Plus_Jakarta_Sans({
  weight: ["400", "500", "600", "700", "800"],
  subsets: ["latin"],
  variable: "--font-jakarta",
  display: "swap",
});

// Noto Sans — loaded for its comprehensive Unicode/IPA coverage.
// Acts as a harmonious fallback for any character not in Plus Jakarta Sans
// or JetBrains Mono, preventing the OS from substituting a mismatched
// system font that renders IPA glyphs at incorrect weight or size.
const notoSans = Noto_Sans({
  weight: ["400", "500", "700"],
  subsets: ["latin"],
  variable: "--font-noto",
  display: "swap",
});

// Gilroy — locally-hosted display face used for the Cyrillic locales (Russian
// and Ukrainian; includes Cyrillic). Applied via the `html[lang="free-ru"]` and
// `html[lang="uk"]` selectors in globals.css; English keeps Jakarta untouched.
// Ships the weights the UI actually uses (light → extrabold); Noto Sans remains
// the fallback for any missing glyph.
const gilroy = localFont({
  src: [
    { path: "./fonts/Gilroy-Light.ttf", weight: "300", style: "normal" },
    { path: "./fonts/Gilroy-Regular.ttf", weight: "400", style: "normal" },
    { path: "./fonts/Gilroy-Medium.ttf", weight: "500", style: "normal" },
    { path: "./fonts/Gilroy-Semibold.ttf", weight: "600", style: "normal" },
    { path: "./fonts/Gilroy-Bold.ttf", weight: "700", style: "normal" },
    { path: "./fonts/Gilroy-Extrabold.ttf", weight: "800", style: "normal" },
  ],
  variable: "--font-gilroy",
  display: "swap",
});


