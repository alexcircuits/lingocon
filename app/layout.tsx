export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const locale = await getLocale();
  const messages = await getMessages();

  return (
    <html lang={locale} suppressHydrationWarning>
      <head>
        <link rel="apple-touch-icon" href="/icons/apple-touch-icon.png" />
        <meta name="theme-color" content="#7c5cff" />
        <script
          dangerouslySetInnerHTML={{
            __html: `(function(){try{var p=localStorage.getItem('palette');if(p==='classic'){document.documentElement.setAttribute('data-palette','classic');}}catch(e){}})();`,
          }}
        />
      </head>
      <body className={`${instrumentSerif.variable} ${jetBrainsMono.variable} ${plusJakarta.variable} ${notoSans.variable} ${gilroy.variable} min-h-screen antialiased`}>
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <SessionProvider>
            <NextIntlClientProvider messages={messages}>
              {children}
              <AchievementListener />
              <Toaster
                position="bottom-right"
                toastOptions={{
                  className: "border-border/50 bg-card shadow-soft",
                }}
              />
            </NextIntlClientProvider>
          </SessionProvider>
        </ThemeProvider>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              if ('serviceWorker' in navigator) {
                if ('${process.env.NODE_ENV}' === 'production') {
                  window.addEventListener('load', () => {
                    navigator.serviceWorker.register('/sw.js').catch(() => {});
                  });
                } else {
                  // In development mode, unregister any existing service workers
                  // to prevent them from aggressively caching Webpack HMR chunks
                  // and causing "originalFactory is undefined" errors.
                  navigator.serviceWorker.getRegistrations().then(function(registrations) {
                    for(let registration of registrations) {
                      registration.unregister();
                    }
                  });
                }
              }
            `,
          }}
        />
      </body>
    </html>
  );
}

